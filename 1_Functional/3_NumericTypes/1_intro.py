'''Numeric Types'''
'''
    --> Boolean Truth Values            --> bool
        ->  (0, 1 = False, True)
    --> Integer Numbers(Z)              --> int
        ->  0, +-1, +-2, ...
    --> Rational Numbers(Q)             --> fractions.Fraction
        ->  {p/q | p, q = Z, q != 0}
    --> Real Numbers(R)                 --> float(decimal.Decimal)
        ->  0, -1, 0.125, 1/3, pi, ...
    --> Complex Numbers(C)              --> complex
        ->  {a + bi | a, b = R}
    --> Z < Q < R < C
'''
